package com.algo.beauty.ch32_string_match;

/**
 * KMP 匹配算法 (Knuth-Morris-Pratt)
 * 
 * 【核心原理】
 * KMP 算法的本质是：
 * 当我们在主串与模式串对比的过程中一旦发现某个字符不匹配了，
 * 我们【不想把主串的指针全部回退到起步点重新开始对比】，因为之前已经对比过了，我们知道它是什么。
 * 
 * 那么，我们能跳过多少个不必要的对比呢？
 * KMP引入了一个“好前缀”和“最长可匹配前缀子串与后缀子串”的概念，
 * 即：构造一个 next 数组（也叫失效函数 fail function）。
 * 
 * next[i] 表示：模式串从 0 到 i 这个子串中，最长的、相等的【前缀】和【后缀】的长度减 1。
 * 这个 next 数组能告诉我们：如果主串在 i 的位置跟模式串 j 的位置发生不匹配，模式串的 j 指针应该直接滑动（回退）到哪个位置。
 */
public class KMP {

    /**
     * KMP 字符串匹配函数
     * @param a 主串
     * @param n 主串长度
     * @param b 模式串
     * @param m 模式串长度
     * @return 匹配到主串起始位置下标，-1表示未找到
     */
    public static int kmpSearch(char[] a, int n, char[] b, int m) {
        // 第一步：构建 next 失效数组
        // next 数组的核心作用就是“如果在当前位置坏了，我该拉着模式串退回到哪里”
        int[] next = getNexts(b, m);
        
        int j = 0; // j 是指向模式串的探针
        
        // 第二步：拿着构建好的 next 进行飞速滑动匹配
        for (int i = 0; i < n; ++i) { // i 是指向主串的探针
            // 只要没匹配上，并且 j 还没退回起点 0：
            // 查 next 数组，寻找跳跃的位置。既然这个位置走不通，我们就“大撤退”到 next 数组指引的最佳前缀位置
            while (j > 0 && a[i] != b[j]) { 
                j = next[j - 1] + 1;
            }
            
            // 遇到一直相等的字符，i（由for推进）和 j 齐头往前走
            if (a[i] == b[j]) { 
                ++j;
            }
            
            // j 走到了头，说明整个模式串彻底匹配完成！
            if (j == m) { 
                return i - m + 1; // 此时 i 停在子串末尾，我们要减去长度从而返回它的头下标
            }
        }
        return -1;
    }

    /**
     * 【核心难点】：构建 next 数组 (失效函数)
     * next[i] = k 表示对于模式串的子串 b[0...i] 来说，
     * 以 b[k] 结尾的前缀 和 b[0...i] 的后缀能完美重合。
     * 因此遇到不匹配时，可以将模式串滑动，用这个最长相同前缀去对齐刚才的最长相同后缀。
     */
    private static int[] getNexts(char[] b, int m) {
        int[] next = new int[m];
        next[0] = -1; // 初始化：单字符显然没有前后缀之说
        int k = -1;
        
        for (int i = 1; i < m; ++i) {
            // 当现在的字符与最长前缀后面的字符接不上时
            // k 退而求其次，寻找次长可以匹配的前后缀 (这就是动态规划的思想)
            while (k != -1 && b[k + 1] != b[i]) {
                k = next[k];
            }
            
            // 如果前面的都接合得好好的，k 就能再推进一位
            if (b[k + 1] == b[i]) {
                ++k;
            }
            next[i] = k; // 记住这个最长公共前/后缀长度对应的下标
        }
        return next;
    }
}
